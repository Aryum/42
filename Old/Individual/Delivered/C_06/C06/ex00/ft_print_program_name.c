#include <unistd.h>

void	print_str(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
	write(1, "\n", 1);
}

int	main(int agrc, char **argv)
{
	agrc = 0;
	print_str(argv[agrc]);
	return (0);
}
